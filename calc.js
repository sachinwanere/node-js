function addition(a1,a2){
    return a1+a2;
}
function sub(b1,b2){
    return b2-b1;
}
function mult(c1,c2){
    return c1*c2;
}
function div(d1,d2){
    return d1/d2;
}
let add=addition(20,13)
console.log("Addition is :: " +add)
console.log("Substraction is :: " +sub(100,50))
console.log('Multiplication is :: ' +mult(5,10))
console.log("Division is :: " +div(50,5))